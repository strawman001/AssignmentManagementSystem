package org.assignment.po;

public class AssignmentProjectDoc {
    private Integer assignmentProjectDocID;
    private String documentType;
    private Integer assignmentProjectID;
    private String assignmentProjectDocName;


    public Integer getAssignmentProjectDocID() {
        return assignmentProjectDocID;
    }

    public void setAssignmentProjectDocID(Integer assignmentProjectDocID) {
        this.assignmentProjectDocID = assignmentProjectDocID;
    }

    public String getDocumentType() {
        return documentType;
    }

    public void setDocumentType(String documentType) {
        this.documentType = documentType;
    }

    public Integer getAssignmentProjectID() {
        return assignmentProjectID;
    }

    public void setAssignmentProjectID(Integer assignmentProjectID) {
        this.assignmentProjectID = assignmentProjectID;
    }

    public String getAssignmentProjectDocName() {
        return assignmentProjectDocName;
    }

    public void setAssignmentProjectDocName(String assignmentProjectDocName) {
        this.assignmentProjectDocName = assignmentProjectDocName;
    }

}
