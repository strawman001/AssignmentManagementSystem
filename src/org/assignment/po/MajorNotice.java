package org.assignment.po;

public class MajorNotice {
    private Integer majorNoticeID;
    private String majorCode;
    private String majorNoticeContent;

    public Integer getMajorNoticeID() {
        return majorNoticeID;
    }

    public void setMajorNoticeID(Integer majorNoticeID) {
        this.majorNoticeID = majorNoticeID;
    }

    public String getMajorCode() {
        return majorCode;
    }

    public void setMajorCode(String majorCode) {
        this.majorCode = majorCode;
    }

    public String getMajorNoticeContent() {
        return majorNoticeContent;
    }

    public void setMajorNoticeContent(String majorNoticeContent) {
        this.majorNoticeContent = majorNoticeContent;
    }
}
