package org.assignment.po;

public class CollegeNotice {
    private Integer collegeNoticeID;
    private String collegeCode;
    private String collegeNoticeContent;

    public Integer getCollegeNoticeID() {
        return collegeNoticeID;
    }

    public void setCollegeNoticeID(Integer collegeNoticeID) {
        this.collegeNoticeID = collegeNoticeID;
    }

    public String getCollegeCode() {
        return collegeCode;
    }

    public void setCollegeCode(String collegeCode) {
        this.collegeCode = collegeCode;
    }

    public String getCollegeNoticeContent() {
        return collegeNoticeContent;
    }

    public void setCollegeNoticeContent(String collegeNoticeContent) {
        this.collegeNoticeContent = collegeNoticeContent;
    }
}
